# A program in python to perform Addition, Subtraction, Multiplication, Division, Integer Division, Modulo Division
n1=int(input("Enter first number:"))
n2=int(input("Enter second number:"))
add=n1+n2
sub=n1-n2
multi=n1*n2
div=n1/n2
Idiv=n1//n2
mdiv=n1%n2
print(str(n1)+"+"+str(n2)+"="+str(add))
print(str(n1)+"-"+str(n2)+"="+str(sub))
print(str(n1)+"*"+str(n2)+"="+str(multi))
print(str(n1)+"/"+str(n2)+"="+str(div))
print(str(n1)+"//"+str(n2)+"="+str(Idiv))
print(str(n1)+"%"+str(n2)+"="+str(mdiv))
